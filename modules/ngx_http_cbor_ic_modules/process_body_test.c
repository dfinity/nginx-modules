ngx_http_cbor_req_ic_ctx_t *test_mk_ctx()
{
    ngx_http_cbor_req_ic_ctx_t *ctx = malloc(sizeof(ngx_http_cbor_req_ic_ctx_t));
    assert(ctx != NULL);

    nullify_str(&ctx->method_name);
    nullify_str(&ctx->request_type);

    ctx->canister_id.data = malloc(64);
    ctx->sender.data = malloc(64);

    assert(ctx->canister_id.data != NULL);
    assert(ctx->sender.data != NULL);

    ZERO(ctx->canister_id.data);
    ZERO(ctx->sender.data);

    return ctx;
}

int main()
{
    buf_t b;
    b.start = &cbor_request_call[0];
    b.end = &cbor_request_call[146];

    ngx_http_cbor_req_ic_ctx_t *ctx = test_mk_ctx();

    process_body(b, ctx);
    assert(strncmp(ctx->canister_id.data, "rrkah-fqaaa-aaaaa-aaaaq-cai", ctx->canister_id.len) == 0);
    assert(strncmp(ctx->sender.data, "2vxsx-fae", ctx->sender.len) == 0);
    assert(strncmp(ctx->method_name.data, "set", ctx->method_name.len) == 0);
    assert(strncmp(ctx->request_type.data, "call", ctx->request_type.len) == 0);

    b.start = &cbor_request_query[0];
    b.end = &cbor_request_query[118];

    ctx = test_mk_ctx();
    process_body(b, ctx);
    assert(strncmp(ctx->canister_id.data, "z2rt2-eaaaa-aaaal-abcva-cai", ctx->canister_id.len) == 0);
    assert(strncmp(ctx->sender.data, "2vxsx-fae", ctx->sender.len) == 0);
    assert(strncmp(ctx->method_name.data, "butts", ctx->method_name.len) == 0);
    assert(strncmp(ctx->request_type.data, "query", ctx->request_type.len) == 0);
}
