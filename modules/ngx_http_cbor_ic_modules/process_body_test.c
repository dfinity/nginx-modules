void nullify_str(ngx_str_t *s)
{
    s->data = NULL;
    s->len = 0;
}

ngx_http_cbor_req_ic_ctx_t *test_mk_ctx()
{
    ngx_http_cbor_req_ic_ctx_t *ctx = malloc(sizeof(ngx_http_cbor_req_ic_ctx_t));
    assert(ctx != NULL);

    nullify_str(&ctx->method_name);
    nullify_str(&ctx->request_type);

    ctx->canister_id.data = malloc(64);
    ctx->sender.data = malloc(64);

    assert(ctx->canister_id.data != NULL);
    assert(ctx->sender.data != NULL);

    ZERO(ctx->canister_id.data);
    ZERO(ctx->sender.data);

    return ctx;
}

